// Створіть калькулятор комісії для HELL_YEAH:
// - Якщо сума транзакції < 1000 UAH - комісія 1%
// - Якщо сума >= 1000 і < 5000 UAH - комісія 0.5%
// - Якщо сума >= 5000 UAH - комісія 0.25%
//
// Створіть змінну transactionAmount = 3500
// Обчисліть комісію згідно умов
// Виведіть: "Сума: 3500 UAH, Комісія: 17.50 UAH"
// ═══════════════════════════════════════════════════════════════════════

void exercise1() {
  print('📝 ЗАВДАННЯ 1: КАЛЬКУЛЯТОР КОМІСІЇ З УМОВАМИ');
  print('-------------------------------------------');

  // ВАШ КОД ТУТ:
  double transactionAmount = 3500;
  double commission;

  if (transactionAmount < 1000) {
    commission = transactionAmount * 0.01;
  } else if (transactionAmount >= 1000 && transactionAmount < 5000) {
    commission = transactionAmount * 0.005;
  } else {
    commission = transactionAmount * 0.0025;
  }

  print(
    'Сума: $transactionAmount UAH, Комісія: ${commission.toStringAsFixed(2)} UAH',
  );

  print('');
}

