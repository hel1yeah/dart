// ПРОГРАМА 4: STRING INTERPOLATION
// ═══════════════════════════════════════════════════════════════════════
void stringInterpolation() {
  String name = "Марія";
  int age = 28;
  double balance = 15000.50;

  // Проста інтерполяція з $
  print('Привіт, $name!');

  // Інтерполяція з виразами ${...}
  print('Наступного року тобі буде ${age + 1} років');

  // Обчислення в рядку
  print('Твій баланс: ${balance.toStringAsFixed(2)} UAH');

  // Багаторядковий текст
  String message =
      '''
Користувач: $name
Вік: $age років
Баланс: ${balance.toStringAsFixed(2)} UAH
Статус: ${balance > 10000 ? "VIP" : "Звичайний"}
  ''';

  print(message);

  // Конкатенація рядків
  String greeting = 'Привіт, ' + name + '!';
  print(greeting);
}

