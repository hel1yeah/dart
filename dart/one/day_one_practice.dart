// ПРОГРАМА 3: NULL SAFETY ПРИКЛАДИ
// ═══════════════════════════════════════════════════════════════════════
void nullSafetyExample() {
  // Nullable змінні (можуть бути null)
  String? email; // null за замовчуванням
  String? phone = "123-456-789";
  String? middleName;

  print('Email: ${email ?? "Не вказано"}'); // ?? - значення за замовчуванням
  print('Телефон: ${phone ?? "Не вказано"}');
  print('По батькові: ${middleName ?? "Не вказано"}');

  // Non-nullable змінні (не можуть бути null)
  String firstName = "Олександр";
  String lastName = "Петренко";

  print('Ім\'я: $firstName');
  print('Прізвище: $lastName');

  // Оператор ! (force unwrap - впевнені що не null)
  String? userName = "ivan123";
  if (userName != null) {
    print('Довжина імені користувача: ${userName.length}');
  }

  // Безпечний виклик методу з ?.
  String? userEmail;
  print('Довжина email: ${userEmail?.length ?? 0}'); // Виведе 0

  userEmail = "user@wanda.exchange";
  print('Довжина email: ${userEmail?.length ?? 0}'); // Виведе 21

  int? test = 42;

  print(test);
}

