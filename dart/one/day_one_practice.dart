void variablesExample() {
  // var - автоматичне визначення типу
  var userName = "Олександр";
  var userAge = 25;

  userName = 123;

  // int - цілі числа
  int balance = 10000;
  int transactions = 42;

  // double - дробові числа
  double exchangeRate = 41.25;
  double commission = 0.5;

  // String - текст
  String currency = "UAH";
  String walletAddress = "0x1234567890abcdef";

  // bool - true/false
  bool isVerified = true;
  bool hasBonus = false;

  // final - незмінна змінна (визначається під час виконання)
  final String appName = "Wanda Exchange";

  // const - константа (визначається під час компіляції)
  const double PI = 3.14159;

  // Виведення
  print('Користувач: $userName');
  print('Вік: $userAge');
  print('Баланс: $balance $currency');
  print('Транзакцій: $transactions');
  print('Курс обміну: $exchangeRate');
  print('Комісія: $commission%');
  print('Адреса гаманця: $walletAddress');
  print('Верифікований: $isVerified');
  print('Є бонус: $hasBonus');
  print('Додаток: $appName');
  print('Число PI: $PI');
}

