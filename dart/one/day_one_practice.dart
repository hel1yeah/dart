// ПРОГРАМА 5: PRINT РІЗНИХ ТИПІВ ДАНИХ
// ═══════════════════════════════════════════════════════════════════════
void printDifferentTypes() {
  // Цілі числа
  int positiveNumber = 100;
  int negativeNumber = -50;
  int zero = 0;

  print('Додатнє число: $positiveNumber');
  print('Від\'ємне число: $negativeNumber');
  print('Нуль: $zero');

  // Дробові числа
  double price = 99.99;
  double rate = 0.025;
  double bigNumber = 1234567.89;

  print('Ціна: $price');
  print('Ставка: $rate');
  print('Велике число: $bigNumber');

  // Текст
  String shortText = "Короткий текст";
  String longText =
      "Це довгий текст, який містить багато інформації про Wanda Exchange";
  String emptyString = "";

  print('Короткий: "$shortText"');
  print('Довгий: "$longText"');
  print('Порожній: "$emptyString"');

  // Логічні значення
  bool isTrue = true;
  bool isFalse = false;

  print('Істина: $isTrue');
  print('Хиба: $isFalse');

  // Арифметичні операції
  print('\nАрифметичні операції:');
  print('100 + 50 = ${100 + 50}');
  print('100 - 50 = ${100 - 50}');
  print('100 * 2 = ${100 * 2}');
  print('100 / 4 = ${100 / 4}');
  print('100 ~/ 3 = ${100 ~/ 3}'); // Цілочисельне ділення
  print('100 % 3 = ${100 % 3}'); // Залишок від ділення
}


